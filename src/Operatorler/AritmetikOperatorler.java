package Operatorler;

public class AritmetikOperatorler {
    public static void main(String[] args){
        int sayi1 = 20;
        int sayi2 = 40;

        System.out.println("Sayı 1 = " + sayi1 + " Sayı 2 = " + sayi2 + " Toplamları = " + (sayi1+sayi2));
        System.out.println("Farkları = " + (sayi1-sayi2) );
        System.out.println("Çarpımları = " + (sayi1*sayi2));
        System.out.println("Bölümleri = " + (sayi1/sayi2));
        System.out.println("Sayı 1 mod Sayı 2 = " + (sayi1 % sayi2));






    }
}
